#include "libft.h"

int		ft_wdcounter(char const *s, char c)
{
	int	n;

	n = 0;
	while (*s != '\0')
	{
		if (*s == c && *s != '\0')
			s++;
		else
		{
			while (*s != c && *s != '\0')
				s++;
			n++;
		}
	}
	return (n);
}
